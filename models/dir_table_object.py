class DirTableObject:
    def __init__(self, id, tkn, line, vci):
        self.id = id
        self.tkn = tkn
        self.line = line
        self.vci = vci


def __repr__(self):
    return (
        f"DirTableObject(id={self.id}, tkn={self.tkn}, "
        f"line={self.line}, vci={self.vci})"
    )
